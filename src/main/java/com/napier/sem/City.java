package com.napier.sem;

/**
 * Represents a city
 */
public class City {
    /**
     * City ID
     */
    public int city_id;

    /**
     * Name of city
     */
    public String name;

    /**
     * Country code of city
     */
    public String country_code;

    /**
     * District city resides within
     */
    public String district;

    /**
     * Population of city
     */
    public int population;
}
